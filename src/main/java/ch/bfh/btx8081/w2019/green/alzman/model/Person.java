package ch.bfh.btx8081.w2019.green.alzman.model;


public class Person {

	int id;
	String firstName;
	String lastName;
	int age;

	public Person(int id, String firstName, String lastName, int age) {
		super();
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.age = age;
	}

}
